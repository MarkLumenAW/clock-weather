export function weatherIcon(code) {
  switch (code) {
    // sun
    case 1000:
      return 'bi:sun-fill';
    // cloud
    case 1003:
    case 1006:
    case 1009:
      return 'bi:cloud-fill';
    // rain
    case 1063:
    case 1150:
    case 1153:
    case 1180:
    case 1183:
    case 1186:
    case 1189:
    case 1192:
    case 1195:
    case 1240:
    case 1243:
    case 1246:
    case 1273:
    case 1276:
    case 1072:
    case 1087:
    case 1237:
    case 1261:
    case 1264:
      return 'bi:cloud-rain-fill';
    // snow
    case 1066:
    case 1114:
    case 1117:
    case 1210:
    case 1213:
    case 1216:
    case 1219:
    case 1222:
    case 1225:
    case 1255:
    case 1258:
    case 1279:
    case 1282:
      return 'bi:cloud-snow-fill';
    // sleet
    case 1069:
    case 1204:
    case 1207:
    case 1249:
    case 1252:
      return 'bi:cloud-sleet-fill';
    // fog
    case 1030:
    case 1135:
    case 1147:
      return 'bi:cloud-fog-fill';
    default:
      return 'line-md:loading-twotone-loop';
  }
}
